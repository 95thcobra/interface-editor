package com.greg.selection;

public class DragModel {

    Double sceneX;
    Double sceneY;
    Double translateX;
    Double translateY;

    public DragModel(double sceneX, double sceneY, double translateX, double translateY) {
        this.sceneX = sceneX;
        this.sceneY = sceneY;
        this.translateX = translateX;
        this.translateY = translateY;
    }
}
